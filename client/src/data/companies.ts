/**
 * Get company suggestions based on search query
 * @param query - The search query string
 * @param limit - Maximum number of suggestions to return
 * @returns Array of matching company names
 */
export function getCompanySuggestions(query: string, limit: number = 10): string[] {
  if (!query || query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  // First, find companies that start with the query (higher priority)
  const startsWithMatches = popularCompanies.filter(company =>
    company.toLowerCase().startsWith(normalizedQuery)
  );
  
  // Then, find companies that contain the query but don't start with it
  const containsMatches = popularCompanies.filter(company =>
    company.toLowerCase().includes(normalizedQuery) &&
    !company.toLowerCase().startsWith(normalizedQuery)
  );
  
  // Combine and limit results
  return [...startsWithMatches, ...containsMatches].slice(0, limit);
}
