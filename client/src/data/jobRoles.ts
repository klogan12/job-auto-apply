/**
 * Get job role suggestions based on search query
 * @param query - The search query string
 * @param limit - Maximum number of suggestions to return
 * @returns Array of matching job role names
 */
export function getJobRoleSuggestions(query: string, limit: number = 10): string[] {
  if (!query || query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  // First, find roles that start with the query (higher priority)
  const startsWithMatches = popularJobRoles.filter(role =>
    role.toLowerCase().startsWith(normalizedQuery)
  );
  
  // Then, find roles that contain the query but don't start with it
  const containsMatches = popularJobRoles.filter(role =>
    role.toLowerCase().includes(normalizedQuery) &&
    !role.toLowerCase().startsWith(normalizedQuery)
  );
  
  // Combine and limit results
  return [...startsWithMatches, ...containsMatches].slice(0, limit);
}
